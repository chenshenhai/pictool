const chai = require('chai');
const digit = require('../dist/digit');
const expect = chai.expect

const transform = digit.transform;

describe( 'test: Pictool.digit.transform', ( ) => {
  it('transform.RGB2HSL', ( done ) => {
    const rgb = {r: 12, g: 15, b: 10};
    const hsl = transform.RGB2HSL(rgb);
    const expectResult = {h:96, s:20, l:5};
    expect(hsl).to.deep.equal(expectResult)
    done()
  });

  it('transform.HSL2RGB', ( done ) => {
    const hsl = {h:96, s:20, l:5} ;
    const rgb = transform.HSL2RGB(hsl);
    const expectResult = {r: 12, g: 15, b: 10};
    expect(rgb).to.deep.equal(expectResult)
    done()
  })
})